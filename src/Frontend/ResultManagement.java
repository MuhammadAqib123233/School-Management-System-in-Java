    private void addresultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addresultActionPerformed
        // TODO add your handling code here:
        ArrayList<Double> obtainedmarks = new ArrayList<>();
        ArrayList<Double> totalmarks = new ArrayList<>();
        Double sumofobmarks=0.0;
        Double sumoftotalmarks=0.0;
        for(int i=0;i<subjectmarksinput.size();i++){
            JTextField sbmarks = subjectmarksinput.get(i);
            JTextField sbmarksoutof = subjectmarksoutof.get(i);
            sumofobmarks = sumofobmarks + Double.parseDouble(sbmarks.getText());
            sumoftotalmarks = sumoftotalmarks + Double.parseDouble(sbmarksoutof.getText());
            obtainedmarks.add(Double.parseDouble(sbmarks.getText()));
            totalmarks.add(Double.parseDouble(sbmarksoutof.getText()));
        }
        Double Percentage = sumofobmarks/sumoftotalmarks * 100;
        String obmarks = new Gson().toJson(obtainedmarks);
        String Grade;
        String Status;
        if(Percentage >=90 && Percentage <100){
            Grade = "A1";
            Status = "Passes";
            System.out.print(Grade);
        }
        else if(Percentage >=80 && Percentage <90){
            Grade = "A+";
            Status = "Passes";
            System.out.print(Grade);
        }
        else if(Percentage >=80 && Percentage <90){
            Grade = "A+";
            Status = "Passes";
            System.out.print(Grade);
        }
        else if(Percentage >=70 && Percentage <80){
            Grade = "A";
            Status = "Passes";
            System.out.print(Grade);
        }
        else if(Percentage >=60 && Percentage <70){
            Grade = "B";
            Status = "Passes";
            System.out.print(Grade);
        }
        else if(Percentage >=50 && Percentage <60){
            Grade = "C";
            Status = "Passes";
            System.out.print(Grade);
        }
        else if(Percentage >=40 && Percentage <50){
            Grade = "D";
            Status = "Passes";
            System.out.print(Grade);
        }
        else if(Percentage >=33 && Percentage <40){
            Grade = "E";
            Status = "Passes";
            System.out.print(Grade);
        }
        else{
            Grade = "null";
            Status = "Fail";
            System.out.print(Grade);
        }
        TableModel model = allstudentsbysection.getModel();
        int i = allstudentsbysection.getSelectedRow();
        int id = (int) model.getValueAt(i, 0);
        boolean result = db.addResult(obmarks, sumofobmarks, sumoftotalmarks, Percentage, Grade, Status, id);
        if(result){
            JOptionPane.showMessageDialog(this, "result has been added");
        }
        else{
            JOptionPane.showMessageDialog(this, "result has not been added");
        }
    }//GEN-LAST:event_addresultActionPerformed

