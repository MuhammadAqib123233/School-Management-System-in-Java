    private void class_selectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_class_selectionActionPerformed
        // TODO add your handling code here:
        section_selection.removeAllItems();
        jPanel3.removeAll();
        jPanel3.revalidate();
        jPanel3.repaint();
        jPanel3.removeAll();
        subjectmarksinput.clear();
        subjectmarksoutof.clear();
        int index = class_selection.getSelectedIndex();
        classes = allclasses.get(index);
        allsections = sections.allSectionsByClass(classes.getClassID());
        for(int i=0;i<allsections.size();i++){
            sections = allsections.get(i);
            section_selection.addItem(sections.getSectionName());
        }
        allsubject = subjectBean.getallSubjectsByClass(classes.getClassID());
        int y1= 20;
        int y2 = 20;
        for(j=0;j<allsubject.size();j++){
            JTextField a = new JTextField();
            JTextField b = new JTextField();
            subjectmarksinput.add(a);
            subjectmarksoutof.add(b);
        }
        for(int i =0;i<subjectmarksinput.size();i++){
            subjectBean = allsubject.get(i);
            JLabel subjectname = new JLabel(subjectBean.getSubjectName());
            JLabel outof = new JLabel("Out Of");
            JTextField si = subjectmarksinput.get(i);
            JTextField sout = subjectmarksoutof.get(i);
            subjectname.setBounds(20, y1, 100, 30);
            si.setBounds(130, y2,70, 30);
            outof.setBounds(210, y2, 70, 30);
            sout.setBounds(290, y2,70, 30);
            jPanel3.add(subjectname);
            jPanel3.add(si);
            jPanel3.add(sout);
            jPanel3.add(outof);
            y1=y1+50;
            y2 = y2 + 50;
        }
    }//GEN-LAST:event_class_selectionActionPerformed

    private void section_selectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_section_selectionActionPerformed
        // TODO add your handling code here:
        allStudents = students.getAllStudentsBySection((String) section_selection.getSelectedItem());
        DefaultTableModel model = (DefaultTableModel)allstudentsbysection.getModel();
        Object[] rowData = new Object[4];
        for(int i=0;i<model.getRowCount();i++){
            model.removeRow(i);
        }
        for(int i =0;i<allStudents.size();i++){
            students = allStudents.get(i);
            rowData[0] = students.getStudentId();
            rowData[1] = students.getFirstname();
            rowData[2] = students.getLastname();
            rowData[3] = students.getFathername();
            model.addRow(rowData);
        }
    }//GEN-LAST:event_section_selectionActionPerformed

